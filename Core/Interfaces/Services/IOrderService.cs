using invoice.Core.DTO;
using invoice.Core.DTO.Order;
using invoice.Core.Enums;

namespace invoice.Core.Interfaces.Services
{
    public interface IOrderService
    {
        Task<GeneralResponse<OrderReadDTO>> CreateAsync(OrderCreateDTO dto, string userId);
        Task<GeneralResponse<IEnumerable<OrderReadDTO>>> CreateRangeAsync(IEnumerable<OrderCreateDTO> dtos, string userId);
        Task<GeneralResponse<OrderReadDTO>> UpdateAsync(string id, OrderUpdateDTO dto, string userId);
        Task<GeneralResponse<IEnumerable<OrderReadDTO>>> UpdateRangeAsync(IEnumerable<OrderUpdateDTO> dtos, string userId);
        Task<GeneralResponse<bool>> DeleteAsync(string id, string userId);
        Task<GeneralResponse<bool>> DeleteRangeAsync(IEnumerable<string> ids, string userId);

        Task<GeneralResponse<OrderReadDTO>> GetByIdAsync(string id, string userId);
        Task<GeneralResponse<IEnumerable<OrderReadDTO>>> GetAllAsync(string userId);
        Task<GeneralResponse<IEnumerable<OrderReadDTO>>> GetByClientAsync(string clientId, string userId);
        Task<GeneralResponse<IEnumerable<OrderReadDTO>>> GetByStatusAsync(OrderStatus status, string userId);

        Task<GeneralResponse<decimal>> GetTotalRevenueAsync(string userId);

        Task<int> CountAsync(string userId);
        Task<bool> ExistsAsync(string id, string userId);
        Task<int> CountByStatusAsync(OrderStatus status, string userId);
    }
}
